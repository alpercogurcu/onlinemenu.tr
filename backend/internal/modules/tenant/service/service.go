// --- TenantWriter ---

// Create inserts a new tenant and publishes the tenant.created.v1 event.
//
// uuid.Nil is used as the RLS context because the tenant row does not exist yet at
// INSERT time. The tenants RLS WITH CHECK requires id = app.tenant_id; with uuid.Nil
// set and a new UUID generated by the DB, the INSERT bypasses the policy — this is an
// accepted bootstrap pattern restricted to the admin/onboarding path only.
// TODO(platform): replace with a dedicated WithBootstrapTx that uses app_migrator role.
func (s *Service) Create(ctx context.Context, t pub.Tenant) (pub.Tenant, error) {
	var created pub.Tenant
	err := s.db.WithTenantTx(ctx, uuid.Nil, func(tx pgx.Tx) error {
		var err error
		created, err = s.tenantRepo.Create(ctx, tx, t)
		return err
	})
	if err != nil {
		return pub.Tenant{}, fmt.Errorf("service: create tenant: %w", err)
	}

	if err := events.PublishTenantCreated(ctx, s.publisher, created); err != nil {
		// Event failure is non-fatal: the DB row is committed and an outbox worker
		// (ADR-DATA-001) is the authoritative delivery mechanism. Log for alerting.
		s.logger.Warn("tenant.created.v1 publish failed",
			zap.String("tenant_id", created.ID.String()),
			zap.Error(err),
		)
	}
	return created, nil
}

