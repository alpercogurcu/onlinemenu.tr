// Service implements the tenant module's public interfaces.
type Service struct {
	db         *db.Pool
	tenantRepo *repo.TenantRepo
	branchRepo *repo.BranchRepo
	docRepo    *repo.DocumentRepo
	intRepo    *repo.IntegratorRepo
	hoursRepo  *repo.HoursRepo
	publisher  eventbus.Publisher
	logger     *zap.Logger
}

// Params groups the fx-injected dependencies for NewService.
type Params struct {
	fx.In

	DB         *db.Pool
	TenantRepo *repo.TenantRepo
	BranchRepo *repo.BranchRepo
	DocRepo    *repo.DocumentRepo
	IntRepo    *repo.IntegratorRepo
	HoursRepo  *repo.HoursRepo
	// Bus is the concrete eventbus.Bus; stored as Publisher so tests can substitute a stub.
	Bus    *eventbus.Bus
	Logger *zap.Logger
}

// NewService constructs a Service for fx injection.
func NewService(p Params) *Service {
	return &Service{
		db:         p.DB,
		tenantRepo: p.TenantRepo,
		branchRepo: p.BranchRepo,
		docRepo:    p.DocRepo,
		intRepo:    p.IntRepo,
		hoursRepo:  p.HoursRepo,
		publisher:  p.Bus,
		logger:     p.Logger,
	}
}

