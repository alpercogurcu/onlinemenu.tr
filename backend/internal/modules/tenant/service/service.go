// --- helpers ---

// wrapNotFound returns sentinel errors (ErrNotFound, ErrInvalid) unwrapped so callers
// can use errors.Is. All other errors are wrapped with the supplied format string.
func wrapNotFound(err error, format string) error {
	if errors.Is(err, pub.ErrNotFound) || errors.Is(err, pub.ErrInvalid) {
		return err
	}
	return fmt.Errorf(format, err)
}

// maxDocumentSize is the upper bound for uploaded document file size (50 MB).
const maxDocumentSize int64 = 50 << 20

var allowedMIMETypes = map[string]bool{
	"application/pdf": true,
	"image/jpeg":      true,
	"image/png":       true,
	"image/webp":      true,
}

func validateDocument(doc pub.Document) error {
	if doc.FileKey == "" || doc.FileName == "" {
		return pub.ErrInvalid
	}
	if doc.FileSize <= 0 || doc.FileSize > maxDocumentSize {
		return pub.ErrInvalid
	}
	if !allowedMIMETypes[doc.MimeType] {
		return pub.ErrInvalid
	}
	if !validDocumentTypeStr(string(doc.DocumentType)) {
		return pub.ErrInvalid
	}
	return nil
}

func validateBranchDocument(doc pub.BranchDocument) error {
	if doc.FileKey == "" || doc.FileName == "" {
		return pub.ErrInvalid
	}
	if doc.FileSize <= 0 || doc.FileSize > maxDocumentSize {
		return pub.ErrInvalid
	}
	if !allowedMIMETypes[doc.MimeType] {
		return pub.ErrInvalid
	}
	if !validBranchDocumentTypeStr(string(doc.DocumentType)) {
		return pub.ErrInvalid
	}
	return nil
}

func validDocumentStatus(s pub.DocumentStatus) bool {
	switch s {
	case pub.DocStatusPending, pub.DocStatusVerified, pub.DocStatusRejected, pub.DocStatusExpired:
		return true
	}
	return false
}

func validDocumentTypeStr(s string) bool {
	switch pub.DocumentType(s) {
	case pub.DocVergiLevhasi, pub.DocTicaretSicil, pub.DocImzaSirkuleri,
		pub.DocFaaliyetBelgesi, pub.DocGidaSicil, pub.DocIsyeriAcmaRuhsati, pub.DocOther:
		return true
	}
	return false
}

func validBranchDocumentTypeStr(s string) bool {
	switch pub.BranchDocumentType(s) {
	case pub.BranchDocVergiLevhasi, pub.BranchDocIsyeriRuhsati, pub.BranchDocKiraSozlesmesi,
		pub.BranchDocFranchiseSozlesmesi, pub.BranchDocGidaSicil, pub.BranchDocIsyeriAcmaRuhsati,
		pub.BranchDocYanginGuvenlik, pub.BranchDocSaglikSertifikasi, pub.BranchDocOther:
		return true
	}
	return false
}
